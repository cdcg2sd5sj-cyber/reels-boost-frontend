function AccordionCard({ card, open, onToggle }: { card: Card; open: boolean; onToggle: () => void }) {
  return (
    <div style={CARD}>
      <button
        onClick={onToggle}
        style={{
          width: '100%', display: 'flex', alignItems: 'center', gap: 10, padding: 14,
          background: 'transparent', border: 'none', cursor: 'pointer', textAlign: 'left' as const,
        }}
      >
        <span style={{ fontSize: 18, flexShrink: 0 }}>{card.emoji}</span>
        <span style={{ flex: 1, fontSize: 13, fontWeight: 700, color: '#fff', lineHeight: 1.4 }}>{card.title}</span>
        <ChevronDownIcon open={open} />
      </button>
      {open && (
        <div style={{ padding: '0 14px 16px', borderTop: '0.5px solid rgba(255,255,255,0.06)', marginTop: -1 }}>
          <div style={{ paddingTop: 14 }}>{card.body}</div>
        </div>
      )}
    </div>
  )
}

